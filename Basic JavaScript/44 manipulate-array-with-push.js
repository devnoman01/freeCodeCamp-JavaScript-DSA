//  manipulate array with push()

//  use push() to add element into array
 
const arr1 = [1, 2, 3];
arr1.push(4);
//  after push now arr1 = [1, 2, 3, 4]

const arr2 = ["Stimpson", "J", "cat"];
arr2.push(["happy", "joy"]);
//  after push arr2 = ["Stimpson", "J", "cat", ["happy", "joy"]]

const myArray = [["John", 23], ["cat", 2]];
myArray.push(["dog", 3]);
//  after push