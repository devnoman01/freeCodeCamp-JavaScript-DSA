//  reusing code with function

//  function is constructed with funtion keyword and () with function-name
//  instructions/statements will be inside {} curly brackets
function functionName() {
    console.log("Hello World");
}
functionName();
//  as we call the function, it will initialize the statement inside it


function reusableFunction(){
    console.log("Hi World");
}
reusableFunction();
//  after calling reusableFunction(), we will get output: Hi World.
//  we don't need to print "Hi World" again and again, just call the function