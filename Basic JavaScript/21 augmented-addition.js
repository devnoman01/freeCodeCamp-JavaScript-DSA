//  augmented addition

let a = 3;
let b = 17;
let c = 12;

a += 12;        // here 12 will be added with the value of a
b += 9;         // here 9 will be added with the value of b
c += 7;         // here 7 will be added with the value of c

//  '+='  assignment operator is used for augmented addition