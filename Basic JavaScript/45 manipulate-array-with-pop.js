//  manipulate array with pop()

//  use pop() to remove last item from array

const threeArr = [1, 4, 6];
const oneDown = threeArr.pop();
console.log(oneDown);               //  output of oneDown: 6
console.log(threeArr);              //  output of threeArr: [1, 4]

const myArray = [["John", 23], ["cat", 2]];
myArray.pop();
//  now myArray = [["John", 23]];